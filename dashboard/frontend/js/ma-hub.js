    function renderMainTable(projects) {
        var el = document.getElementById('ma-main-table');
        if (!el) return;

        var groups = {};
        STAGE_ORDER.forEach(function (s) { groups[s] = []; });
        projects.forEach(function (p) {
            if (DEAD_GROUPS.indexOf(p.group) === -1 && groups[p.group]) {
                groups[p.group].push(p);
            }
        });

        var html = '';
        STAGE_ORDER.forEach(function (stage) {
            var items = groups[stage];
            if (!items || !items.length) return;
            var sc = STAGE_COLORS[stage] || '#6b7280';

            html += '<div style="margin-bottom:20px">'
                + '<div style="display:flex;align-items:center;gap:8px;padding:10px 12px;background:'
                + sc + '18;border-left:3px solid ' + sc + ';border-radius:6px;margin-bottom:8px">'
                + '<span style="font-weight:700;color:' + sc + '">' + esc(stage) + '</span>'
                + '<span class="pl-chip" style="font-size:11px;background:' + sc + '30;color:' + sc + '">'
                + items.length + ' deals</span></div>';

            html += '<div style="overflow-x:auto"><table class="pl-deals-table"><thead><tr>'
                + '<th>Deal</th><th>POC</th><th>Status</th><th>Priority</th>'
                + '<th>Deal Type</th><th>Deal Source</th><th>Valuation</th>'
                + '<th>Target %</th><th>IC Score</th><th>CDD</th><th>Updated</th>'
                + '</tr></thead><tbody>';

            items.sort(function (a, b) { return (b.updated_at || '').localeCompare(a.updated_at || ''); });

            items.forEach(function (p) {
                var cols = p.columns || {};
                var val = cols['Valuation'] ? fmtK(cols['Valuation']) : (p.value ? fmtK(p.value) : '-');
                var eq = cols['Target Equity Purchase'] ? cols['Target Equity Purchase'] + '%' : '-';
                var ic = cols['Latest IC Score (Avg.)'] || '';
                var icC = ic ? (Number(ic) >= 70 ? '#34d399' : Number(ic) >= 50 ? '#f59e0b' : '#ef4444') : '';
                var subs = p.subitems_count > 0 ? p.subitems_complete + '/' + p.subitems_count : '-';

                html += '<tr>'
                    + '<td class="pl-deal-name">' + esc(p.name) + '</td>'
                    + '<td class="text-muted-sm">' + esc(cols['POC'] || '-') + '</td>'
                    + '<td>' + chip(p.status) + '</td>'
                    + '<td>' + chip(cols['Priority'] || '-', PRIORITY_COLORS[cols['Priority']]) + '</td>'
                    + '<td class="text-muted-sm">' + esc(cols['Deal Type'] || '-') + '</td>'
                    + '<td class="text-muted-sm">' + esc(cols['Deal Source'] || '-') + '</td>'
                    + '<td style="font-weight:600">' + val + '</td>'
                    + '<td>' + esc(eq) + '</td>'
                    + '<td style="font-weight:700;' + (icC ? 'color:' + icC : '') + '">' + (ic || '-') + '</td>'
                    + '<td class="text-muted-sm">' + subs + '</td>'
                    + '<td class="text-muted-sm">' + relDate(p.updated_at) + '</td>'
                    + '</tr>';
            });

            // Summary row
            var totalVal = items.reduce(function (s, p) {
                return s + Number((p.columns || {})['Valuation'] || p.value || 0);
            }, 0);
            var scoredItems = items.filter(function (p) { return (p.columns || {})['Latest IC Score (Avg.)']; });
            var avgIC = scoredItems.length
                ? (scoredItems.reduce(function (s, p) { return s + Number(p.columns['Latest IC Score (Avg.)']); }, 0) / scoredItems.length).toFixed(0)
                : '-';

            html += '<tr style="background:var(--card-bg);font-weight:600;border-top:2px solid var(--card-border)">'
                + '<td colspan="6" style="text-align:right;color:var(--text-muted)">Group Total</td>'
                + '<td>' + (totalVal > 0 ? fmtK(totalVal) : '-') + '</td>'
                + '<td></td><td>' + avgIC + '</td><td colspan="2"></td></tr>';

            html += '</tbody></table></div></div>';
        });

        el.innerHTML = html || '<div class="pl-empty">No active deals</div>';
    }

