    function renderAllScores(projects) {
        var el = document.getElementById('ma-scores-bars');
        if (!el) return;

        var scored = projects
            .filter(function (p) {
                return (p.columns || {})['Latest IC Score (Avg.)'] && Number(p.columns['Latest IC Score (Avg.)']) > 0;
            })
            .map(function (p) {
                return { name: p.name, score: Number(p.columns['Latest IC Score (Avg.)']) };
            })
            .sort(function (a, b) { return b.score - a.score; });

        if (!scored.length) { el.innerHTML = '<div class="pl-empty">No scored deals</div>'; return; }

        var html = '';
        scored.forEach(function (item) {
            var pct = Math.min(100, Math.max(3, item.score));
            var c = item.score >= 70 ? '#34d399' : item.score >= 50 ? '#f59e0b' : '#ef4444';
            html += '<div class="stat-bar-item">'
                + '<div class="stat-bar-header">'
                + '<span class="stat-bar-label">' + esc(item.name) + '</span>'
                + '<span class="stat-bar-value" style="color:' + c + '">' + item.score + '%</span></div>'
                + '<div class="stat-bar-track">'
                + '<div class="stat-bar-fill" style="width:' + pct.toFixed(1) + '%;background:' + c
                + '"></div></div></div>';
        });
        el.innerHTML = html;
    }

