    function renderIMsChart(data) {
        var months = data.items_by_month || {};
        if (!window.Chart || !Object.keys(months).length) {
            var el = document.getElementById('ma-ims-chart');
            if (el) el.innerHTML = '<div class="pl-empty">No chart data</div>';
            return;
        }
        var canvas = window.ensureCanvas('ma-ims-chart', 220);
        if (!canvas) return;

        var entries = Object.entries(months).sort();
        var labels = entries.map(function (e) {
            var m = parseInt(e[0].split('-')[1], 10) - 1;
            return (window.MONTH_SHORT || [])[m] || e[0];
        });
        var values = entries.map(function (e) { return e[1]; });

        var opts = window.chartOpts ? window.chartOpts(false) : {
            responsive: true, maintainAspectRatio: false,
            plugins: { legend: { display: false } }
        };
        var chart = new Chart(canvas, {
            type: 'bar',
            data: {
                labels: labels,
                datasets: [{
                    data: values,
                    backgroundColor: '#3CB4ADCC', hoverBackgroundColor: '#3CB4AD',
                    borderColor: '#3CB4AD', borderWidth: 1, borderRadius: 4, barPercentage: 0.7
                }]
            },
            options: opts
        });
        if (window.storeChart) window.storeChart('ma-ims-chart', chart);
    }

