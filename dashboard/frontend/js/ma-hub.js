    function renderICDistribution(icItems) {
        var el = document.getElementById('ma-ic-dist');
        if (!el) return;

        var buckets = [
            { label: '80\u2013100%', min: 80, max: 101, count: 0 },
            { label: '70\u201379%', min: 70, max: 80, count: 0 },
            { label: '50\u201369%', min: 50, max: 70, count: 0 },
            { label: '30\u201349%', min: 30, max: 50, count: 0 },
            { label: '1\u201329%', min: 1, max: 30, count: 0 }
        ];
        icItems.forEach(function (item) {
            var s = item.scores ? (item.scores['Latest IC Score (Avg.)'] || 0) : 0;
            for (var i = 0; i < buckets.length; i++) {
                if (s >= buckets[i].min && s < buckets[i].max) { buckets[i].count++; break; }
            }
        });

        var mx = Math.max.apply(null, buckets.map(function (b) { return b.count; })) || 1;
        var colors = ['#34d399', '#3CB4AD', '#334FB4', '#f59e0b', '#ef4444'];
        var html = '';
        buckets.forEach(function (b, i) {
            var pct = Math.max(3, (b.count / mx) * 100);
            html += '<div class="stat-bar-item"><div class="stat-bar-header">'
                + '<span class="stat-bar-label">' + b.label + '</span>'
                + '<span class="stat-bar-value">' + b.count + ' deals</span></div>'
                + '<div class="stat-bar-track"><div class="stat-bar-fill" style="width:' + pct.toFixed(1)
                + '%;background:' + colors[i] + '"></div></div></div>';
        });
        el.innerHTML = html;
    }

    function renderCDDProgress(projects) {
        var el = document.getElementById('ma-cdd-progress');
        if (!el) return;

        var withSubs = projects.filter(function (p) {
            if (DEAD_GROUPS.indexOf(p.group) !== -1) return false;
            return (p.subitems && p.subitems.length > 0) || p.subitems_count > 0;
        }).sort(function (a, b) {
            var pA = a.subitems_count > 0 ? a.subitems_complete / a.subitems_count : 0;
            var pB = b.subitems_count > 0 ? b.subitems_complete / b.subitems_count : 0;
            return pB - pA;
        });

        if (!withSubs.length) { el.innerHTML = '<div class="pl-empty">No CDD data</div>'; return; }

        var html = '';
        withSubs.forEach(function (p) {
            var subs = p.subitems || [];
            var total = subs.length || p.subitems_count || 0;
            var done = subs.length ? subs.filter(function (s) { return s.done; }).length : (p.subitems_complete || 0);
            var pct = total > 0 ? Math.round((done / total) * 100) : 0;
            var c = pct === 100 ? '#34d399' : pct >= 50 ? '#f59e0b' : '#ef4444';

            html += '<div style="margin-bottom:10px">'
                + '<div style="display:flex;justify-content:space-between;margin-bottom:4px;font-size:12px">'
                + '<span style="color:var(--text);font-weight:600">' + esc(p.name) + '</span>'
                + '<span style="color:' + c + ';font-weight:700">' + done + '/' + total
                + ' (' + pct + '%)</span></div>'
                + '<div style="height:6px;background:var(--card-border);border-radius:3px;overflow:hidden">'
                + '<div style="height:100%;width:' + pct + '%;background:' + c
                + ';border-radius:3px;transition:width .6s cubic-bezier(.25,.1,.25,1)"></div></div>';

            if (subs.length) {
                html += '<div style="display:flex;gap:4px;margin-top:4px;flex-wrap:wrap">';
                subs.forEach(function (s) {
                    var sc = s.done ? '#34d399' : '#6b7280';
                    html += '<span style="font-size:10px;padding:2px 6px;border-radius:4px;background:'
                        + sc + '20;color:' + sc + '">' + (s.done ? '\u2713 ' : '') + esc(s.name) + '</span>';
                });
                html += '</div>';
            }
            html += '</div>';
        });
        el.innerHTML = html;
    }

