    window.renderMAHub = function () {
        var data = window.MONDAY;
        if (!data || !data.projects) {
            var el = document.getElementById('ma-main-table');
            if (el) el.innerHTML = '<div class="pl-empty">No Monday.com data loaded. Run generate_monday_dashboard.py to sync.</div>';
            return;
        }

        renderMainTable(data.projects);
        renderPerfKPIs(data.projects);
        renderIMsChart(data);
        renderFlowStatusChart(data);
        renderAllScores(data.projects);
        renderICKPIs(data.ic_items || []);
        renderScorecardTable(data.projects);
        renderICDistribution(data.ic_items || []);
        renderCDDProgress(data.projects);
    };
