    function esc(s) { var d = document.createElement('div'); d.textContent = s || ''; return d.innerHTML; }
    function fmtK(v) {
        if (!v) return '\u00a30';
        v = Number(v);
        if (v >= 1e6) return '\u00a3' + (v / 1e6).toFixed(1) + 'M';
        if (v >= 1e3) return '\u00a3' + (v / 1e3).toFixed(0) + 'K';
        return '\u00a3' + v.toLocaleString('en-GB');
    }
    function relDate(iso) {
        if (!iso) return '';
        var diff = Math.floor((Date.now() - new Date(iso).getTime()) / 86400000);
        if (diff === 0) return 'Today';
        if (diff === 1) return 'Yesterday';
        if (diff < 7) return diff + 'd ago';
        if (diff < 30) return Math.floor(diff / 7) + 'w ago';
        return Math.floor(diff / 30) + 'mo ago';
    }

    function chip(text, color) {
        if (!text || text === '-') return '<span class="text-muted-sm">-</span>';
        var bg = color || STATUS_COLORS[text] || '#6b7280';
        return '<span class="pl-chip" style="font-size:11px;background:' + bg + '20;color:' + bg
            + ';border:1px solid ' + bg + '40">' + esc(text) + '</span>';
    }

    function scoreCell(val, label) {
        if (!val || val === '0' || val === 0) return '<td class="text-muted-sm">-</td>';
        var n = Number(val);
        var c = n >= 70 ? '#34d399' : n >= 50 ? '#f59e0b' : '#ef4444';
        return '<td style="font-weight:700;color:' + c + '" title="' + esc(label || '') + '">' + n + '</td>';
    }

    // ── Tab switching ──
