        'Gate 0': '#334FB4', 'Gate 1': '#334FB4', 'Gate 2': '#334FB4', 'Gate 3': '#334FB4',
        'IM Received': '#1e293b', 'Pending Info': '#f59e0b', 'Pending scorecard': '#f59e0b',
